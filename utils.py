def get_stock_collection(collection_value: str) -> str:
    """
    Normalize and return the stock collection value from the 'collection' column.
    
    Args:
        collection_value: The value from the 'collection' column
        
    Returns:
        Normalized collection name, or 'Unknown' if not available
    """
    if pd.isna(collection_value) or not str(collection_value).strip():
        return "Unknown"
    return str(collection_value).strip()


def classify_stocks_collection_in_df(df: pd.DataFrame) -> pd.DataFrame:
    """
    Add a _stock_collection column based on the 'collection' column in the DataFrame.
    
    The input Excel file must have a 'collection' column that specifies the stock
    center/source (e.g., 'Bloomington', 'Kyoto', 'VDRC', etc.).
    
    Args:
        df: DataFrame with stock data (must have 'collection' column)
        
    Returns:
        DataFrame with _stock_collection column added
    """
    df = df.copy()
    
    # Look for the collection column (case-insensitive)
    collection_col = None
    for col in df.columns:
        if col.lower() == 'collection':
            collection_col = col
            break
    
    if collection_col is None:
        raise ValueError(
            "Input Excel file must have a 'collection' column specifying the stock center "
            "(e.g., 'Bloomington', 'Kyoto', 'VDRC'). Column not found."
        )
    
    # Check for custom stocks and override collection
    def get_collection_with_custom_check(row):
        is_custom = row.get("is_custom_stock", False)
        stock_num = str(row.get("stock_number", "")).upper()
        if is_custom or stock_num == "CUSTOM":
            return "Custom Lab Generated"
        return get_stock_collection(row.get(collection_col, ""))
    
    df["_stock_collection"] = df.apply(get_collection_with_custom_check, axis=1)
    return df


def get_collection_sort_key(collection: str, collection_stock_counts: Dict[str, int]) -> tuple:
    """
    Generate a sort key for collections.
    
    Priority:
    1. Bloomington (0)
    2. Custom Lab Generated (1)
    3. Others (2, sorted by descending stock count)
    
    Args:
        collection: Collection name
        collection_stock_counts: Dict mapping collection -> stock count
    
    Returns:
        Tuple (priority, count_sort_key)
    """
    collection_lower = str(collection).lower().strip()
    stock_count = collection_stock_counts.get(collection, 0)
    
    if "bloomington" in collection_lower or collection_lower == "bdsc":
        return (0, 0)
    elif "custom lab generated" in collection_lower:
        return (1, 0)
    else:
        return (2, -stock_count)


