###############################################################################
# ID Cleaning and Parsing Utilities
###############################################################################

def clean_id(val) -> str:
    """
    Clean an ID value (PMID, PMCID, stock number, etc.) to a normalized string.
    
    Handles:
    - None/NaN values -> ""
    - Float-like strings ending in ".0" -> removes suffix
    - "nan" strings -> ""
    - Whitespace trimming
    
    Args:
        val: Any value that might be an ID
    
    Returns:
        Cleaned string, or empty string if invalid
    """
    if pd.isna(val):
        return ""
    val_str = str(val).strip()
    if val_str.endswith('.0'):
        val_str = val_str[:-2]
    return val_str if val_str.lower() != "nan" else ""


def is_valid_id(val) -> bool:
    """Check if a value represents a valid ID (not empty/NaN)."""
    return bool(clean_id(val))


def parse_semicolon_list(values_str: str) -> List[str]:
    """Parse a semicolon-separated string into a list of cleaned values."""
    if pd.isna(values_str) or not str(values_str).strip():
        return []
    return [v.strip() for v in str(values_str).split(';') if v.strip()]


def join_semicolon_list(values: List[str], sort: bool = False) -> str:
    """Join a list of values into a semicolon-separated string."""
    if not values:
        return ""
    return '; '.join(sorted(set(values)) if sort else values)


def filter_semicolon_values(
    values_str: str,
    exclude_set: set,
    value_mapping: Dict[str, str] = None
) -> str:
    """
    Filter semicolon-separated values, excluding those in exclude_set.
    
    Args:
        values_str: Semicolon-separated string of values
        exclude_set: Set of values to exclude
        value_mapping: Optional dict to map input values to output values
    
    Returns:
        Filtered semicolon-separated string
    """
    values = parse_semicolon_list(values_str)
    if value_mapping:
        filtered = [value_mapping.get(v, v) for v in values if v not in exclude_set and value_mapping.get(v)]
    else:
        filtered = [v for v in values if v not in exclude_set]
    return join_semicolon_list(filtered)


