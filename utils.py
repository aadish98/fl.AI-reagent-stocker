def generate_keyword_column_name(keywords: List[str]) -> str:
    """
    Generate the column name for keyword matching based on the keywords.
    
    Args:
        keywords: List of keywords
    
    Returns:
        Column name like "sleep OR circadian in title/ abstract of publication?"
    """
    if not keywords:
        return "Keyword in title/ abstract of publication?"
    return f"{' OR '.join(keywords)} in title/ abstract of publication?"


def find_keyword_column(df: pd.DataFrame) -> Optional[str]:
    """
    Find the GPT-derived 'PMID of <keywords> references that showed stocks
    functional validity' column.

    This column is produced by the GPT functional-validation step and is
    **not** the same as the title/abstract keyword-hit column.  Used by
    ``get_gpt_derived_columns`` to tag GPT-derived data.

    Returns the column name if found, None otherwise.
    """
    pattern = re.compile(r"^PMID of .+ references that showed stocks functional validity$")
    for col in df.columns:
        if pattern.match(str(col)):
            return col
    return None


def find_keyword_title_abstract_column(df: pd.DataFrame) -> Optional[str]:
    """
    Find the title/abstract keyword-hit boolean column produced by
    ``pipeline_references``.

    The column is named like
    ``"sleep OR circadian in title/ abstract of publication?"``
    (generated by :func:`generate_keyword_column_name`).

    Returns the column name if found, None otherwise.
    """
    pattern = re.compile(r"in title/ abstract of publication\?$")
    for col in df.columns:
        if pattern.search(str(col)):
            return col
    return None


def extract_keywords_from_title_abstract_column(col_name: str) -> List[str]:
    """
    Extract keywords from a title/abstract column name like
    ``"sleep OR circadian in title/ abstract of publication?"``.

    Returns list of keywords (e.g., ``['sleep', 'circadian']``).
    """
    match = re.match(
        r"^(.+?)\s+in title/ abstract of publication\?$", col_name
    )
    if not match:
        return []

    keywords_str = match.group(1)
    # "Keyword" is the placeholder when no keywords were supplied
    if keywords_str == "Keyword":
        return []
    keywords = [kw.strip() for kw in keywords_str.split(" OR ")]
    return keywords


def extract_keywords_from_column(col_name: str) -> List[str]:
    """
    Extract keywords from a column name.

    Supports both the title/abstract column
    (``"sleep OR circadian in title/ abstract of publication?"``) and the
    legacy GPT-derived column
    (``"PMID of sleep OR circadian references that showed stocks functional
    validity"``).

    Returns list of keywords (e.g., ``['sleep', 'circadian']``).
    """
    # Try title/abstract pattern first (preferred)
    result = extract_keywords_from_title_abstract_column(col_name)
    if result:
        return result

    # Legacy GPT-derived pattern
    match = re.match(r"^PMID of (.+) references that showed stocks functional validity$", col_name)
    if not match:
        return []

    keywords_str = match.group(1)
    keywords = [kw.strip() for kw in keywords_str.split(" OR ")]
    return keywords


def has_keyword_validation(value) -> bool:
    """
    Check if a stock has validation in keyword-matching references.
    
    Returns True if the 'PMID of <kw> references that showed stocks functional validity'
    column has content.
    """
    if pd.isna(value):
        return False
    return bool(str(value).strip())


###############################################################################
# GPT-Derived / Experimental Column Handling
###############################################################################

# Static names of columns whose values come from GPT / functional validation
GPT_DERIVED_COLUMNS_STATIC = frozenset({
    "Functional Validity (by reference)?",
    "Functionally Valid Stock?",
    "phenotypes_observed",
    "validation_rationale",
})

# Prefix used to mark experimental/GPT-derived cell values
EXPERIMENTAL_PREFIX = "[EXPERIMENTAL] "


def get_gpt_derived_columns(df: pd.DataFrame) -> List[str]:
    """
    Return list of GPT-derived column names present in a DataFrame.

    Includes both static column names and the dynamic
    'PMID of <keywords> references that showed stocks functional validity' column.
    """
    cols = [c for c in GPT_DERIVED_COLUMNS_STATIC if c in df.columns]
    # Dynamic keyword validation column
    kw_col = find_keyword_column(df)
    if kw_col and kw_col in df.columns:
        cols.append(kw_col)
    return cols


def apply_experimental_prefix(df: pd.DataFrame) -> pd.DataFrame:
    """
    Return a copy of *df* with '[EXPERIMENTAL] ' prefixed to the **column
    headers** of GPT-derived columns.  Cell values are left unchanged.
    Avoids double-prefixing if the header already starts with the prefix.
    """
    gpt_cols = get_gpt_derived_columns(df)
    if not gpt_cols:
        return df

    df = df.copy()
    rename_map = {}
    for col in gpt_cols:
        if not str(col).startswith(EXPERIMENTAL_PREFIX):
            rename_map[col] = f"{EXPERIMENTAL_PREFIX}{col}"
    if rename_map:
        df = df.rename(columns=rename_map)
    return df


def load_keywords(filepath: Path) -> List[str]:
    """
    Load keywords from a text file (one keyword/phrase per line).
    
    Args:
        filepath: Path to the keywords file
    
    Returns:
        List of lowercase keywords
    """
    keywords = []
    with open(filepath, 'r', encoding='utf-8') as f:
        for line in f:
            kw = line.strip().lower()
            if kw and not kw.startswith('#'):  # Skip empty lines and comments
                keywords.append(kw)
    return keywords


